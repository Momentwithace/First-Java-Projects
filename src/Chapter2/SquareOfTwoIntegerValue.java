package Chapter2;

public class SquareOfTwoIntegerValue {
    public static void main(String[] args) {
        int firstNumber = 5;
        int secondNumber = 6;
        int sum = 0;
        sum += firstNumber + secondNumber;
        sum = 11;
        double S = Math.pow(11, 2);

        System.out.println("The square of "+sum+" is " + S);
    }
}
